interface printMaze {
   default char[][] printMaze(String[] inputMaze) {
        char[][] maze = new char[inputMaze.length][inputMaze[0].length()];
        iterateMazeRows(inputMaze, maze);
       return maze;
    }

    private static void iterateMazeRows(String[] inputMaze, char[][] maze) {
        for (int i = 0; i < inputMaze.length; i++) {
            maze[i] = inputMaze[i].toCharArray();
        };
    };
};